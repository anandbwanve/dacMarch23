class pyramid{
public static void main(String argss[]){
	
	for(int i=1; i<10; i++){
		for(int j=1; j<=i; j++){
		System.out.print("* ");
		}
		System.out.println( " ");
		
	}
    for(int i=10; i>0; i--){
		for(int j=1; j<=i; j++){
		System.out.print("* ");
		}
		System.out.println( " ");
    }
	}
}
       